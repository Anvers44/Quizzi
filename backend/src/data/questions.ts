// Attach stable IDs
export const QUESTION_BANK: Question[] = RAW.map((q, i) => ({
  ...q,
  id: `${q.theme}_${String(i).padStart(3, "0")}`,
}));

// ─── Pick questions ────────────────────────────────────────────────────
export function pickQuestions(
  themes: string[],
  difficulty: "easy" | "medium" | "hard" | "all",
  count: number,
): Question[] {
  const useAllThemes = themes.includes("all") || themes.length === 0;

  let pool = QUESTION_BANK.filter((q) => {
    const themeOk = useAllThemes || themes.includes(q.theme);
    const diffOk = difficulty === "all" || q.difficulty === difficulty;
    return themeOk && diffOk;
  });

  // Shuffle
  for (let i = pool.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [pool[i], pool[j]] = [pool[j], pool[i]];
  }

  if (pool.length >= count) return pool.slice(0, count);

  // Fallback: ignore difficulty filter
  const fallback = QUESTION_BANK.filter(
    (q) => useAllThemes || themes.includes(q.theme),
  )
    .filter((q) => !pool.some((p) => p.id === q.id))
    .sort(() => Math.random() - 0.5);

  return [...pool, ...fallback].slice(0, count);
}
